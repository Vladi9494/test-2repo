# СПИСОК ОБЪЕКТОВ

class Smartphone:
    def __init__(self, brand, model, number):
        self.brand = brand
        self.model = model
        self. number = number

        def get_brand(self):
            return self.brand

        def get_model(self):
            return self.model

        def get_brand_and_model_and_number(self):
            return f'{brand}- {model}. {number}'
